#include "3-calc.h"

/**
* op_add - adds a and b
* @a: first integer
* @b: second integer
* Return: a+b
*/

int op_add(int a, int b);
	return (a + b);

/**
* op_sub - subtracts a and b
* @a: first integer
* @b: second integer
* Return: a-b
*/

int op_sub(int a, int b);
	return (a - b);

/**
* op_mult - multiplies a and b
* @a: first integer
* @b: second integer
* Return: a*b
*/

int op_mul(int a, int b);
	return (a * b);
/**
* op_div - divides a and b
* @a: first integer
* @b: second integer
* Return: a/b
*/

int op_div(int a, int b);
	return (a / b);
/**
* op_mod - finds remainder of a/b
* @a: first integer
* @b: second integer
* Return: remainder of a/b
*/

int op_mod(int a, int b);
	return (a % b);

